import java.util.*;

class Solution {
    private int[] dp;

    public int combinationSum4(int[] nums, int target) {
        dp = new int[target + 1];
        Arrays.fill(dp, -1);
        return combinationSum4Helper(nums, target);
    }

    private int combinationSum4Helper(int[] nums, int target) {
        if (target == 0) {
            return 1;
        }
        if (target < 0) {
            return 0;
        }
        if (dp[target] != -1) {
            return dp[target];
        }

        int ways = 0;
        for (int num : nums) {
            ways += combinationSum4Helper(nums, target - num);
        }
        dp[target] = ways;
        return ways;
    }
}
